class Penguin {

    void swim()
	{
        System.out.println("Penguin is swimming.");
    }

    void walk() 
	{
        System.out.println("Penguin is walking.");
    }

    void slide()
	{
        System.out.println("Penguin is sliding on ice.");
    }

    void eat() 
	{
        System.out.println("Penguin is eating fish.");
    }

    void hunt()
	{
        System.out.println("Penguin is hunting in water.");
    }

    void dive()
	{
        System.out.println("Penguin is diving deep.");
    }

    void rest() 
	{
        System.out.println("Penguin is resting.");
    }

    void protectEgg() 
	{
        System.out.println("Penguin is protecting its egg.");
    }

    void buildNest()
	{
        System.out.println("Penguin is building a nest.");
    }

    void huddle()
	{
        System.out.println("Penguins are huddling for warmth.");
    }

    void flapWings() 
	{
        System.out.println("Penguin is flapping its wings.");
    }

    void makeSound() 
	{
        System.out.println("Penguin is making sound.");
    }

    void migrate()
	{
        System.out.println("Penguin is migrating.");
    }

    void floatWater()
	{
        System.out.println("Penguin is floating in water.");
    }

    void cleanFeathers()
	{
        System.out.println("Penguin is cleaning its feathers.");
    }

    static void species()
	{
        System.out.println("Species is emperor penguin");
    }

    static void habitat()
	{
        System.out.println("Habitat is antarctica");
    }

    static void lifeSpan()
	{
        System.out.println("Lifespan is 15 to 20 years");
    }

    static void color()
	{
        System.out.println("Color is black and white");
    }

    static void type()
	{
        System.out.println("Type is flightless bird");
    }
}