class Bridge {

    void construct()
	{
        System.out.println("Bridge construction started.");
    }

    void openBridge() 
	{
        System.out.println("Bridge is open for traffic.");
    }

    void closeBridge()
	{
        System.out.println("Bridge is closed for maintenance.");
    }

    void allowVehicles() 
	{
        System.out.println("Vehicles are passing on the bridge.");
    }

    void allowPedestrians() 
	{
        System.out.println("Pedestrians are walking on the bridge.");
    }

    void inspect()
	{
        System.out.println("Bridge inspection is in progress.");
    }

    void repair() 
	{
        System.out.println("Bridge repair work started.");
    }

    void clean()
	{
        System.out.println("Bridge is being cleaned.");
    }

    void paint()
	{
        System.out.println("Bridge is being painted.");
    }

    void lightUp()
	{
        System.out.println("Bridge lights are turned on.");
    }

    void withstandWind() 
	{
        System.out.println("Bridge withstands strong winds.");
    }

    void withstandEarthquake() 
	{
        System.out.println("Bridge is designed to resist earthquakes.");
    }

    void collectToll() 
	{
        System.out.println("Toll is collected from vehicles.");
    }

    void monitorTraffic() 
	{
        System.out.println("Traffic is being monitored.");
    }

    void supportWeight()
	{
        System.out.println("Bridge supports heavy vehicles.");
    }

    static void bridgeType() 
	{
        System.out.println("Type is Suspension Bridge");
    }

    static void material() 
	{
        System.out.println("Material are Steel and Concrete");
    }

    static void length() 
	{
        System.out.println("Length is 2 kilometers");
    }

    static void yearBuilt() 
	{
        System.out.println("Built in 2010");
    }

    static void location() 
	{
        System.out.println("Location is across the river");
    }
}