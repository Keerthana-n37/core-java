class Blanket{
    String material;
    String size;
    String color;
    double price;
    String brand;
    double weight;

    Blanket(String material, 
	        String size, 
			String color, 
			double price, String brand, double weight){
        this.material = material;
        this.size = size;
        this.color = color;
        this.price = price;
        this.brand = brand;
        this.weight = weight;
    }
}