class Racket {
    String brand;
    String type;
    double weight;
    double price;
    String gripSize;
    String color;
    String material;
    String stringType;
    double length;
    String country;

    Racket(String brand, 
	       String type, 
		   double weight, 
		   double price, 
		   String gripSize,
           String color,
		   String material, 
		   String stringType, 
		   double length, 
		   String country) {

        this.brand = brand;
        this.type = type;
        this.weight = weight;
        this.price = price;
        this.gripSize = gripSize;
        this.color = color;
        this.material = material;
        this.stringType = stringType;
        this.length = length;
        this.country = country;
    }
}
