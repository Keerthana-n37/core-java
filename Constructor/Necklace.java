class Necklace {
    String material;
    double price;
    String color;
    double weight;
    String brand;
    String design;
    String type;
    int length;
    String stoneType;
    String occasion;
    String claspType;
    String country;
    String warranty;
    String plating;
    String gender;
    String finish;
    String collection;
    String availability;
    String shape;
    String style;

    Necklace(String material, double price, String color, double weight, String brand,
             String design, String type, int length, String stoneType, String occasion,
             String claspType, String country, String warranty, String plating,
             String gender, String finish, String collection, String availability,
             String shape, String style) {

        this.material = material;
        this.price = price;
        this.color = color;
        this.weight = weight;
        this.brand = brand;
        this.design = design;
        this.type = type;
        this.length = length;
        this.stoneType = stoneType;
        this.occasion = occasion;
        this.claspType = claspType;
        this.country = country;
        this.warranty = warranty;
        this.plating = plating;
        this.gender = gender;
        this.finish = finish;
        this.collection = collection;
        this.availability = availability;
        this.shape = shape;
        this.style = style;
    }
}
