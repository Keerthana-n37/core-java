class ShoeLace {
    String brand;
    String color;
    double length;
    String material;
    double price;
    String type;
    String shape;
    String durability;
    String country;
    String size;

    ShoeLace(String brand, String color, double length, String material, double price,
             String type, String shape, String durability, String country, String size) {

        this.brand = brand;
        this.color = color;
        this.length = length;
        this.material = material;
        this.price = price;
        this.type = type;
        this.shape = shape;
        this.durability = durability;
        this.country = country;
        this.size = size;
    }
}